				<h2 class="text-primary">Вероучение</h2>
				<p>
					Систематизированное вероучение евангельских христиан-баптистов было разработано на
					основании Библии. Союз церквей христиан-баптистов, развиваясь на территории Евразии, время
					от времени переиздавал свое вероучение, основой которого был текст, разработанный в
					прошлом веке баптистским проповедником И. Г. Онкеном. Так в 1906 г. было издано вероучение
					христиан-баптистов под редакцией председателя Всероссийского Союза баптистов Д. И. Мазаева
					и в 1928 г. – под редакцией председателя Всесоюзного Союза баптистов Н. В. Одинцова.
					Аналогичное вероучение было принято в 1985 г. на 43 съезде ВСЕХБ. Содержание данного
					вероучения евангельских христиан-баптистов отражает текст вышеуказанных изданий. Надеемся,
					что настоящее вероучение послужит вспомогательным материалом для духовного воспитания и
					возрастания верующих и дальнейшего устройства церквей нашего евангельско-баптистского
					братства, объединенного в Союз церквей ЕХБ. Евангельские христиане-баптисты являются
					церковью возрожденных христиан и в своем вероучении отражают стремление к библейскому
					учению первоапостольской церкви.
				</p>
