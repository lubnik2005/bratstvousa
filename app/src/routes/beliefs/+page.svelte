				<article class="mb-4">
					<h4 class="text-secondary">II. Бог Отец</h4>
					<p>
						Мы веруем в единого, живого, истинного Бога (Ис. 45, 22; Иер. 10, 10; Иоан. 17, 3),
						сотворившего небо, землю и все, что наполняет их (Быт. 1, 1-2; Пс. 23, 1; Ис. 40, 26-29;
						45, 12; Деян. 17, 24-29), Который проявляет Себя в трех лицах: Отец, Сын и Святой Дух, –
						три в одном и один в трех лицах, живущий вечно, «и Сии три суть едино» (Ис. 48, 16; 1
						Иоан. 5, 7; Рим. 11, 33). Бог есть дух (Иоан. 4, 24), и как дух Он невидим для людей
						(Исх. 33, 20; Иоан. 1, 18; 1 Иоан. 4, 12; 1 Тим. 6, 16). Однако «невидимое Его, вечная
						сила Его и Божество, от создания мира чрез рассматривание творений видимы…» (Рим. 1,
						20). Познать Бога человек может только через откровение Бога о Самом Себе, которое
						содержится в Слове Божьем и становится понятным благодаря действию Духа Святого (2 Петр.
						1, 20-21; Иоан. 14, 26).
					</p>
				</article>
				<article class="mb-4">
					<h4 class="text-secondary">III. Бог Сын – Иисус Христос</h4>
					<p>
						Иисус Христос является вечным Богом (1 Иоан. 5, 20) и не имеет ни начала дней, ни конца
						жизни (Евр. 7, 3). Он же – Творец Вселенной (Кол. 1, 16). Прежде создания мира Бог
						предопределил Своего единородного Сына в жертву умилостивления для искупления и спасения
						рода человеческого (1 Петр. 1, 18-20; 2, 24; 3, 18; 1 Иоан. 2, 2; Гал. 4, 4). Он явился
						во плоти (1 Тим. 3, 16), родившись от девы Марии через непорочное зачатие от Духа
						Святого (Матф. 1, 18). Бог Сын соединил в Себе и Божественное, и человеческое естество
						(Лук. 1, 35). Он пришел в мир «в подобии плоти греховной» (Рим. 8, 3), но в Нем не было
						греха (1 Иоан. 3, 5). «Он не сделал никакого греха» (1 Петр. 2, 22). Он непричастен злу,
						поэтому мог стать жертвой за грех мира (Иоан. 1, 29; 2 Кор. 5, 21; Евр. 9, 26). Иисус
						Христос – это обещанный Помазанник Божий и единственный Спаситель мира (Лук. 4, 18-19;
						Иоан. 4, 42; 10, 28; Деян. 2, 36; 4, 12; Еф. 5, 2). Он победил дьявола и ад (Откр. 3,
						21; 5, 5; 1 Кор. 15, 54-55), разрушил смерть (2 Тим. 1, 10). Своей смертью Господь Иисус
						Христос лишил силы имеющего державу смерти, то есть дьявола (Лук. 4:33-36, 41; 1 Иоан.
						3, 8; Евр. 2, 14-15; Откр. 20: 3-4, 14). Он искупил нас Своей Кровью, став единственным
						посредником между Богом и человеком (Кол. 1, 20; 1 Тим. 2, 5-6; Тит. 2, 14; Евр. 7,
						24-25). Он стал Ходатаем за нас перед Отцом, а также Первосвященником будущих благ (1
						Иоан. 2, 1; Евр. 9: 11-12, 24). Верующий в Него имеет жизнь вечную (Иоан. 6, 47).
						Воскресший, вознесшийся на небо и прославленный Иисус Христос является Создателем и
						Главой Церкви (Матф. 16, 18; Еф. 1, 22-23; 5, 23; Кол. 1, 18? 1 Тим. 3, 16). Он будет
						судить вселенную (Иоан. 5, 22; Деян. 17, 31).
					</p>
				</article>

				<article class="mb-4">
					<h4 class="text-secondary">IIII. Бог Дух Святой</h4>
					<p>

Дух Святой является личностью и, будучи Богом (Деян. 5, 3-4), обладает:</p>
					<ul class="list-group list-group-flush">
						<li class="list-group-item">всемогуществом (Рим. 15, 19)</li>
						<li class="list-group-item">всеведением (1 Кор. 2, 10)</li>
						<li class="list-group-item"> вездесущностью (Пс. 138, 7) </li>
						<li class="list-group-item"> святостью (Еф. 4, 30) и другими атрибутами. </li>
					</ul>

          <p>

  
Дух Святой является Творцом вселенной наравне с Отцом и Сыном (Быт. 1, 2; Иов. 33, 4). Дух Святой вдохновлял пророков и Апостолов (2 Петр. 1, 21). В день Пятидесятницы был ниспослан Дух Святой для свидетельства о Христе, для прославления Его и для созидания Церкви (Иоан. 15, 26; 16: 7, 14; Деян. 1, 8; 1 Кор. 12, 13).

Святой Дух обличает мир о грехе, о правде и о суде (Иоан. 16, 8), ведет человека к покаянию и возрождает его. Он вселяется в покаявшегося, обратившегося и повинующегося Богу (Иоан. 3, 3-6; Деян. 5, 32; 1 Кор. 6, 19; Тит. 3, 5).

Дух Святой живет в рожденном свыше (Рим. 8, 9; 1 Кор. 3, 16; Еф. 1, 13), учит (Иоан. 14, 26), обличает (Иоан. 16, 8-9), наставляет (Иоан. 16, 13; 1 Кор. 2, 12-13), руководит (Деян. 8, 29; 13, 2), ободряет и утешает (Иоан. 14, 26), утверждает (Еф. 3: 16-19), производит плод (Гал. 5, 22-23; Еф. 5, 9) и наделяет благодатными дарами для служения в церкви (1 Кор. 12:7-11, 28).

Слово Божье неоднократно повелевает верующим исполняться Духом Святым (Еф. 5. 18; Деян. 2, 4; 4: 8, 31; 1 Кор. 2, 4), чтобы иметь силу для святой жизни и свидетельства об Иисусе Христе (Деян. 1, 8; 4, 31; 1 Кор. 2, 4). Священное Писание повелевает не грешить против Духа Святого (Ис. 63, 10; Марк. 3, 29; Деян. 5: 3, 9; 7, 51; Еф. 4, 30; 1 Фес. 5, 19).

					</p>
