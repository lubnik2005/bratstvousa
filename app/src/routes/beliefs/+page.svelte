				<h3 class="text-primary">3. О СОТВОРЕНИИ МИРА</h3>
				<p>
					Мы веруем, что Бог сотворил небо и землю, солнце, луну и звезды (Быт. 1 и 2 гл.; Деян. 17,
					24), все видимое и невидимое (Пс. 88, 12; 1 Кор. 8, 6; Евр. 11, 3; От кр. 4, 11), проявив
					Свою вечную силу, могущество и многоразличную премудрость (Еф. 3, 9-10; Кол. 1, 16).
				</p>

				<p>
					Бог сотворил также человека по образу Своему и подобию (Быт. 1, 27; 5, 1; Иак. 3, 9). «От
					одной крови Он произвел весь род человеческий для обитания по всему ли цу земли» (Деян.
					17, 26).
				</p>

				<article class="mb-4">
					<h4 class="text-secondary" id="Человек сотворен:">I. Человек сотворен:</h4>
					<ul>
						<li>для владычества над всем живущим на земле (Быт. 1, 26; Пс. 8, 5-9);</li>
						<li>для добрых дел (Еф. 2, 10);</li>
						<li>для прославления Бога (1 Кор. 6, 19-20);</li>
						<li>
							для вечной (Быт. 2: 9, 17), святой (1 Петр. 1, 15-16; Матф. 5, 8) и блаженной жизни в
							непрестанном общении с Богом (Быт. 3, 8; Иоан. 12, 26; 14: 1-2, 23; 17, 2 4; 1 Фес. 4,
							17).
						</li>
					</ul>

					<p>
						Бог сотворил Ангелов (Кол. 1, 16), чтобы они служили Ему (Пс. 102, 20; Евр. 1, 7) и тем,
						кто наследует спасение (Евр. 1, 14). Ангелы – это бесплотные существа ( духи),
						превосходящие людей крепостью и силой (2 Петр. 2, 11), разные по наименованию и степени
						власти (Кол. 1, 16; 1 Фес. 4, 16).
					</p>

					<p>
						Ангелы Божьи всегда находятся перед лицом Отца Небесного (Ис. 6, 1-7), радуются о
						спасении грешников (Лук. 15, 10), наблюдают за жизнью народов и детей (Дан. 10 , 12-21;
						Матф. 18, 10), за жизнью церквей (1 Кор. 11, 10; 1 Тим. 5, 21).
					</p>

					<p>
						Господь посылает Ангелов для наказания наглых хулителей Бога (Деян. 12, 23). Хотя Ангелы
						превосходят человека по своей силе, Слово Божье запрещает поклоняться и м (Откр. 22,
						8-9).
					</p>

					<p>
						Превосходнейший из ангелов не устоял в истине, согрешил (Иез. 28, 16; 1 Иоан. 3, 8),
						сделался человекоубийцей (Иоан. 8, 44) и был назван дьяволом. Вслед за ним некоторые из
						ангелов не сохранили своего достоинства и согрешили, за что Бог связал их узами адского
						мрака (2 Петр. 2, 4; Иуды 6).
					</p>
				</article>
