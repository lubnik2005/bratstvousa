						требования святости Божьей (Рим. 3, 25-26), и теперь спасение по благодати дано всем
						людям (Деян. 28, 28; 2 Петр. 3,9; Рим. 3, 24; Тит. 2, 11). Для получения спасения
						необходима вера, которая происходит от слышания Слова Божьего (Деян. 4, 4; Рим. 10, 17;
