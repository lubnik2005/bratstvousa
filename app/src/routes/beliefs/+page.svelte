				<!-- Триединство Бога -->
				<article class="mb-4">
					<h4 class="text-secondary">I. Триединство Бога</h4>
					<p>
						Мы веруем в единого, живого, истинного Бога (Ис. 45, 22; Иер. 10, 10; Иоан. 17, 3),
						сотворившего небо, землю и все, что наполняет их (Быт. 1, 1-2; Пс. 23, 1; Ис. 40, 26-29;
						45, 12; Деян. 17, 24-29), Который проявляет Себя в трех лицах: Отец, Сын и Святой Дух, –
						три в одном и один в трех лицах, живущий вечно, «и Сии три суть едино» (Ис. 48, 16; 1
						Иоан. 5, 7; Рим. 11, 33). Бог есть дух (Иоан. 4, 24), и как дух Он невидим для людей
						(Исх. 33, 20; Иоан. 1, 18; 1 Иоан. 4, 12; 1 Тим. 6, 16). Однако «невидимое Его, вечная
						сила Его и Божество, от создания мира чрез рассматривание творений видимы…» (Рим. 1,
						20). Познать Бога человек может только через откровение Бога о Самом Себе, которое
						содержится в Слове Божьем и становится понятным благодаря действию Духа Святого (2 Петр.
						1, 20-21; Иоан. 14, 26).
					</p>
