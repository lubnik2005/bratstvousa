				<h3 class="text-primary">2. О СЛОВЕ БОЖЬЕМ</h3>
				<p>
					Мы веруем, что Библия, то есть канонические книги Ветхого (39 книг) и Нового (27 книг)
					Завета, является Словом Божьим, написанным мужами Божьими по вдохновению Духа Святого (2
					Петр. 1, 21) для указания роду человеческому пути ко спасению (2 Тим. 3, 15-16). Всякому
					боящемуся Бога Он отверзает ум к уразумению Писания (Пс. 24, 14; Лук. 10, 22; 24, 45; 2
					Петр. 1, 20), так -что при содействии Духа Святого оно становится для него источником
					Богопознания, вполне достаточным для познан ия истины и воли Божьей относительно жизни и
					служения (2 Тим. 3, 16). Всякое откровение, противоречащее Слову Божьему, – ложно (Ис. 8,
					20).
				</p>

				<p>
					Священное Писание является единственным источником христианской веры (Иоан. 20, 31; Рим.
					10, 17) и духовным руководством для верующих относительно спасения, жиз ни и служения (И.
					Нав. 1, 7-8; 2 Петр. 1, 19; Рим. 15, 4; 1 Тим. 4, 16; Евр. 4, 12).
				</p>

				<p>
					Прибавление к Слову Божьему и убавление, а также извращение текста при переводе или
					толковании влечет за собой строгое наказание от Бога (Откр. 22, 18-19).
				</p>
			</section>
