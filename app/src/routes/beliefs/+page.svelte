						Святого и вкусивший благого глагола Божьего и сил будущего века, достигнет вечной жизни
						только в том случае, если начатую жизнь твердо сохранит до конца (Иез. 18, 24; Евр. 3,
						14; 6, 4-6; 10, 38-39).
					</p>
				</article>
				<article class="mb-4">
					<h4 class="text-secondary">III. Покаяние, обращение, прощение, возрождение</h4>
					<p>
						Дух Святой обличает человека в грехе и открывает ему благость Божью, побуждает его
						покаяться и обратиться к Богу (Иоан. 16, 8; Деян. 2, 31-38; Рим. 2, 4). Человек
						спасается (Деян. 2, 40), если принимает обличение от Духа Святого, кается в грехах,
						обращается от мертвых дел к Богу и принимает Иисуса Христа Спасителем и Господом (Иоан.
						1, 12; Деян. 3, 19; 5, 31; Евр. 6, 1; 9, 14). Покаяние и обращение включает в себя
						сокрушение о грехах (Пс. 37, 19; 2 Кор. 7, 10), исповедание грехов и оставление их
						(Притч. 28, 13; Лук. 18, 13-14; Деян. 19, 18-19; 26, 20). Следствием обращения и личного
						принятия Иисуса Христа Господом и Спасителем является прощение грехов (Деян. 10, 42-43;
						1 Иоан. 1, 9; Кол. 1, 13-14) и рождение от Духа Святого и Слова Божьего, без чего
						невозможно спастись и войти в Царство Божье (Иоан. 1, 12-13; 3, 56; Иак. 1, 18). Через
						рождение свыше человек становится чадом Божьим, причастником Божеского естества и храмом
						Духа Святого (2 Петр. 1, 4; 1 Кор. 6, 19; Евр. 2, 14). Признаки рождения свыше:
					</p>

					<ul>
						<li>перемена образа жизни (2 Кор. 5, 17; Еф. 4, 22; 5, 8; Рим. 6, 16-17);</li>
						<li>
							отвержение нечестия и мирских похотей, ненависть ко злу (Пс. 96, 10; Иак. 1, 21; 2
							Петр. 1, 4; Еф. 4, 22-24; Тит. 2, 12);
						</li>
						<li>исполнение воли Божьей (Пс. 142, 10; Еф. 6, 6);</li>
						<li>
							любовь к Господу и церкви (Иоан. 13, 34; 1 Иоан. 3, 14; 5, 1; 1 Кор. 16, 14; Гал. 5,
							13; Еф. 4, 15);
						</li>
						<li>
							жажда общения с Богом через Библию и молитву (Пс. 54, 18; 62, 2; 118, 10; Деян. 17,
							11);
						</li>
						<li>стремление к уподоблению Христу (Фил. 2, 13-15; 2 Кор. 3, 18; Гал. 4, 19).</li>
					</ul>
				</article>

				<article class="mb-4">
					<h4 class="text-secondary">IV. Оправдание, усыновление</h4>
					<p>
						Верою в Господа Иисуса Христа рожденные свыше получают оправдание даром, по благодати
						Его, искуплением во Христе Иисусе (Рим. 3, 24; Гал. 2, 16). Верою они обретают мир с
						Богом (Рим. 5, 1), получают свидетельство от Духа Святого, что они дети Божьи и
						наследники вечной жизни (1 Иоан. 5, 10-11; Рим. 8, 16-17; Гал. 4, 6-7). Оправдание
						изменяет положение уверовавшего человека перед Богом: он спасен от гнева Божьего (Рим.
						5, 9) и освобожден от вечного осуждения (Рим. 4, 5; 5, 1; 8:1, 30). Уверовавший
						освобождается от виновности за сделанные грехи и от страха осуждения за них (Рим.
						3:24-25, 28; 5, 9; 2 Кор. 5, 21; Гал. 2, 16). Искупленных и оправданных Бог усыновляет,
						и они становятся наследниками Царства Божьего (Рим. 8, 16-18; Гал. 3, 26; 4, 4-5; Еф. 1,
						4-7).
					</p>
				</article>
				<article class="mb-4">
					<h4 class="text-secondary">V. Освящение</h4>
					<p>
						Освящение – это воля Божья для всех искупленных: «Ибо призвал нас Бог не к нечистоте, но
						к святости» (1 Фес. 4, 3-8). Без святости никто не может увидеть Господа (1 Фес. 4, 3-7;
						Евр. 12, 14). При уверовании человек освящается Кровью завета (1 Петр. 1, 1-2; Евр. 10:
						10, 14, 29; 13, 12). Христианин нуждается в постоянном освящении в течение всей жизни
						(Евр. 12, 14; Откр. 22, 11). Руководствуясь Словом Господним и поступая по духу, он
						приносит плод святости (1 Петр. 1: 15-16; Рим. 6, 22; Гал. 5, 16). Освящение – это
						отделение от греха (1 Петр. 2, 9; Тит. 2, 14), посвящение себя Богу и преображение в
						образ Иисуса Христа (1 Иоан. 3, 2-3; Рим. 6: 2, 6, 8, 12-13; 12, 1-2; 2 Кор. 5, 15; Фил.
						2: 5, 15; 1 Фес. 3, 13). Освящение осуществляется Богом (1 Фес. 5, 23) с участием самого
						верующего. Бог действует через жертву Иисуса Христа (Рим. 6, 11-12; 1 Кор. 1, 30; Еф. 5,
						27; 1 Фес. 5, 23; Евр. 10, 10), через Свое Слово (Иоан. 17, 17) и живущего в нас Духа
						Святого (1 Кор. 6, 19-20). К участию человека относится: осуждение, исповедание своих
						грехов и сознание нужды в прощающей благодати Божьей (1 Иоан. 1, 7-9; 1 Кор. 11, 31; 2
						Кор. 7, 1; Евр. 4, 16); вера в очищение Кровью Христа (Евр. 10, 19-22); исследование и
						принятие Слова Божьего с желанием повиноваться ему (Иоан. 5, 39; Деян. 17, 11; Иак. 1,
						21; 1 Петр. 2, 1-2; 1 Тим. 4, 16; 2 Тим. 1, 13; 3, 15-16); святое хождение перед Богом
						под влиянием Духа Святого (Рим. 8, 14; 12, 1-2; Откр. 22, 11); доверие Господу во всех
						обстоятельствах жизни (Иуды 24-25 ст.; 2 Тим. 1, 12). Святая любовь к Господу и Его
						заповедям, под действием Духа Святого, влечет детей Божьих к постоянному освящению (Рим.
						5, 5; 2 Кор. 3, 18; 7, 1; Еф. 4: 15, 23-24).
					</p>
				</article>
				<article class="mb-4">
					<h4 class="text-secondary">VI. Молитва</h4>
					<p>
						Молитва – это средство общения человека с Богом (Пс. 5, 2-4; Фил. 4, 6). В молитве мы
						поклоняемся Богу, славословим Его, говоря о Его величии и славе (1 Пар. 29, 11-15; Пс.
						102, 1-19). В молитве мы открываем свои желания и нужды Богу, Который силен исполнить
						наши прошения (Фил. 4, 6-7). Он отвечает на молитвы тех, кто взывает к Нему с чистым
						сердцем, с верой и по Его воле (Пс. 49, 15; 65, 18; Марк. 11,24; 1 Иоан. З, 21-22).
						«Близок Господь ко всем призывающим Его, ко всем призывающим Его в истине. Желание
						боящихся Его Он исполняет, вопль их слышит, и спасает их» (Пс. 144, 18-19). Слово Божье
						учит нас молиться:
					</p>

					<ul>
						<li>о том, чтобы святилось имя Отца нашего Небесного (Матф. 6, 9);</li>
						<li>о пришествии Царства Его (Матф. 6, 10);</li>
						<li>о том, чтобы воля Его свершалась и на земле, как на небе (Лук. 11, 1-2);</li>
						<li>о себе и родных (Матф. 6, 11-13; 15, 22; 17, 15);</li>
						<li>об успехе распространения Евангелия (1 Тим. 2, 1-4; 1 Фес. 1, 8; 2 Фес. 3, 1);</li>
						<li>о всех святых и о церкви (Еф. 6, 18; Фил. 1, 4);</li>
						<li>о служителях (Матф. 9, 37-38; Рим. 15, 30; Еф. 6, 19);</li>
						<li>
							о больных и страждущих по воле Божьей (Иак. 5, 14-16; 1 Петр. 4, 19; Евр. 13, 3);
						</li>
						<li>о тех, кто обижает и преследует детей Божьих (Матф. 5, 44);</li>
						<li>о правителях, начальтвующих и всех людях (Иер. 29, 7; 1 Тим. 2, 2).</li>
					</ul>

					<p>
						Дети Божьи призваны всегда и за все благодарить Господа, потому что все Его намерения
						служат во благо верующему (Иер. 29, 11; Рим. 8, 28; 1 Фес. 5, 17-18). Апостолы и ученики
						часто молились с постом (Деян. 13, 2; 2 Кор. 11, 27). Согласно Слову Божьему молитвы с
						постом совершаются: при необходимости личного смирения и при желании быть ближе к
						Господу (Ездр. 8, 21а; Ис. 57, 15; Дан. 10, 12); при избрании и рукоположении служителей
						церкви (Деян. 13, 2-3; 14, 23); при общих нуждах народа Божьего (Ездр. 8, 21-23) и
						бедствиях (Иоиль 1, 14; 2, 12-15); при переживаниях, скорбях (2 Цар. 1, 12; Пс. 34, 13;
						68, 11) и других обстоятельствах (Марк. 9, 29; 2 Кор. 6, 4-5). Пост – это не только
						воздержание от принятия пищи и питья и пребывание в молитве, но и проявление
						добродетели, разрешение «оков неправды» и установление должных отношений с людьми (Ис.
						58, 3-12; Матф. 5, 23-24).
					</p>
				</article>
			</section>

			<section id="section-1" class="mb-5 mt-6">
				<h3 class="text-primary">5. О ЦЕРКВИ</h3>

				<p>
					Мы веруем, что Церковь создал Иисус Христос (Матф. 16, 18). Она утверждена на основании
					учения Апостолов и пророков и имеет краеугольным камнем Самого Иисуса Христа (1 Кор. 3,
					11; Еф. 2, 20; Кол. 1, 18). Члены церкви, крещенные одним Духом (1 Кор. 12, 13), являются
					друг для друга членами и составляют одно тело (Рим. 12, 5; 1 Кор. 12, 14), составляемое и
					совокупляемое посредством всяких взаимно скрепляющих связей. Главой этого тела является
					Иисус Христос (Еф. 1, 22; 4, 15-16). Духовное родство влечет святых к объединению, и
					потому христиане, живущие в одной местности, соединяются в местную церковь (Деян. 11, 22;
					13, 1; 2 Кор. 1, 1). Местные церкви, составляя единое тело Господа, соединены между собой
					многоразличными связями. Это усмотрел Господь для участия в нуждах святых (Деян. 11,
					27-30; 1 Иоан. 3, 17; Рим. 15, 25-27; 2 Кор. 8, 14-15; 9, 12-14), для совместного
					разрешения духовных вопросов (Деян. 15: 1-2, 4-29). Связи между церквами становятся более
					крепкими, когда в объединении об общинах заботятся служители, получившие для этого
					благодать от Бога и доверие от церквей (2 Кор. 11, 28; Кол. 4, 7-8; Фил. 2, 19-25). Цель
					их служения – совместное подвизание за веру, однажды преданную святым (Иуды 3 ст.; Фил. 1,
					27-29), устройство местных церквей (1 Тим. 3, 14-15; 5, 17-22; Тит. 1, 5) и
					распространение благой вести о Господе и Спасителе Иисусе Христе (Деян. 16, 1-3; 20, 4;
					Рим. 16, 3-4). Мы верим, что объединение церквей в тесный союз любви и мира, основанный на
					святости, совершает Сам Господь Духом Святым (Пс. 92, 5; Еф. 1, 4; 2, 13; 4, 16).Со дня
					Пятидесятницы существует Вселенская (невидимая) Церковь, созданная Христом, в которую
					входят местные церкви (Деян. 2, 41-47; Еф. 1, 20-23).
				</p>

				<article class="mb-4">
					<h4 class="text-secondary">I. Вселенская Церковь</h4>
					<p>
						Вселенская Церковь состоит из спасенных, рожденных свыше людей (Деян. 2, 47; Тит. 3, 5),
						искупленных Кровью Христа из всякого колена, языка, народа и племени (Кол. 1, 12-22;
						Евр. 12, 22-23; Откр. 5, 9-10), имеющих в себе свидетельство, что они дети Божьи (1
						Иоан. 5, 10-11; Рим. 8, 16). Вселенская Церковь является:
					</p>
					<p></p>
					<ul></ul>

					<ul>
						<li>паствой Христа (Иоан. 10, 16; 21, 15-17; 1 Петр. 5, 2; Евр. 13, 20);</li>
						<li>духовным домом, или храмом Божьим (1 Петр. 2, 5; Еф. 2, 19-21; Евр. 3, 6);</li>
						<li>невестой Христа (2 Кор. 11, 2; Еф. 5, 27; Откр. 19, 7-9; 21, 9-10);</li>
						<li>телом Христа (1 Кор. 12: 12-13, 27; Еф. 1, 22-23; Кол. 1, 17-27);</li>
						<li>столпом и утверждением истины (1 Тим. 3, 15).</li>
					</ul>

					<p>
						Всех уверовавших и рожденных свыше Иисус Христос крестит (погружает) Духом Святым в тело
						Христово (Иоан. 1, 33; Деян. 2, 47; 1 Кор. 12, 13; Гал. 3, 27; Кол. 2, 12). По окончании
						периода благодати Церковь Христова будет восхищена с земли в сретение Господу, чтобы
						всегда быть с Ним, видеть Его и разделять Его славу (Иоан. 17: 22, 24; 1 Кор. 15, 51-57;
						Фил. 3, 20-21; 1 Фес. 4, 13-17).
					</p>
				</article>
			</section>
